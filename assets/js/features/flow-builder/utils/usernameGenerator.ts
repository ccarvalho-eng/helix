export function generateUsername(): string {
  const randomValues = new Uint32Array(3);
  window.crypto.getRandomValues(randomValues);

  const adjective = adjectives[randomValues[0] % adjectives.length];
  const animal = animals[randomValues[1] % animals.length];
  const number = randomValues[2] % 100;

  return `${adjective} ${animal} ${number}`;
}

export function isGeneratedUsername(username: string): boolean {
  // Check if it matches the pattern: Adjective Animal Number
  const parts = username.split(' ');
  if (parts.length !== 3) return false;

  const [adj, animal, num] = parts;
  return adjectives.includes(adj) && animals.includes(animal) && /^\d+$/.test(num);
}
